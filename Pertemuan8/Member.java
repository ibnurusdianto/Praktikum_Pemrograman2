package Pertemuan8;

public class Member {
    private String nama;
    private String jenisMember;

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getNama() {
        return nama;
    }

    public void setJenisMember(String jenisMember) {
        this.jenisMember = jenisMember;
    }

    public String getJenisMember() {
        return jenisMember;
    }
}
